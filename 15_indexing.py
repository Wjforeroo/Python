print(text[1])
#print (text[999])
print(text[-1])
print(text[-2])

#Slicing
print(text[0:5])
print(text[10:16])
print(text[:10])#Empieza en la posicion 0 (Primera posición)
print(text[5:])#Va hasta la ultima posición
print(text[:])#Desde el inicio hasta el final 

print(text[10:16:2]) #EL tercer elemento (2) es el numero de saltos empieza. 
print(text[::2])
